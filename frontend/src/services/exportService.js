/**
 * Generates and downloads a council-ready PDF report.
 *
 * @param {Object} payload
 * @param {Object}  payload.project         - { name, municipality, erfNumber, siteAreaM2, zoningScheme, … }
 * @param {Object}  [payload.zoning]        - { result, params, scheme } from ZoningView
 * @param {Object}  [payload.energy]        - { result, params } from energy compliance
 * @param {Object}  [payload.schedule]      - { doors: [], windows: [] }
 * @param {Object}  [payload.tally]         - { items: [], lightingCount, … }
 * @param {Object}  [payload.spec]          - CompiledSpec { sections: [] }
 */
export function exportPdf(payload) {
  const { project, zoning, energy, schedule, tally, spec } = payload
  const projectName = project?.name ?? 'Unnamed Project'

  const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' })
  doc.setFont('helvetica')
  let y = 0

  // ── Cover / Project Summary ────────────────────────────────────────────────
  y = drawHeader(doc, projectName, 'Council Submission Report')
  y = drawSection(doc, 'Project Information', y)
  y = drawInfoGrid(doc, [
    ['Project Name', project?.name],
    ['Municipality',  project?.municipality ?? '—'],
    ['Erf Number',    project?.erfNumber ?? '—'],
    ['Zoning Scheme', project?.zoningScheme ?? '—'],
    ['Site Area',     project?.siteAreaM2 ? `${project.siteAreaM2} m²` : '—'],
    ['Report Date',   today()],
  ], y)

  // ── Zoning Compliance ──────────────────────────────────────────────────────
  if (zoning?.result) {
    y += 4
    if (y > 250) { doc.addPage(); y = drawHeader(doc, projectName, 'Zoning Compliance') }
    y = drawSection(doc, 'Municipal Zoning Compliance', y)

    const r = zoning.result
    const p = zoning.params ?? {}
    const s = zoning.scheme ?? {}

    // Summary pass/fail
    doc.setFontSize(10)
    doc.setFont('helvetica', 'bold')
    const overallPass = r.isCompliant !== false
    doc.setTextColor(...(overallPass ? [22, 163, 74] : [220, 38, 38]))
    doc.text(overallPass ? '✓  OVERALL: COMPLIANT' : '✗  OVERALL: NON-COMPLIANT', 12, y)
    doc.setTextColor(...DARK)
    y += 8

    // Build table from checks array (each has rule, description, providedValue, requiredValue, unit, passed)
    const checkRows = (r.checks ?? []).map((c) => [
      c.description ?? c.rule ?? '',
      c.providedValue != null ? `${Number(c.providedValue).toFixed(2)} ${c.unit ?? ''}`.trim() : '—',
      c.requiredValue != null ? `${Number(c.requiredValue).toFixed(2)} ${c.unit ?? ''}`.trim() : '—',
      passFailBadge(c.passed),
    ])

    // Fallback: if no checks but we have params+scheme, build rows manually
    const tableRows = checkRows.length ? checkRows : [
      ['Coverage %',    pct(p.footprintM2 && p.siteAreaM2 ? (p.footprintM2 / p.siteAreaM2) * 100 : null),
                        pct(s.maxCoveragePercent), '—'],
      ['FAR', num(p.proposedGfaM2 && p.siteAreaM2 ? p.proposedGfaM2 / p.siteAreaM2 : null, 3),
              num(s.maxFar, 1), '—'],
    ]

    autoTable(doc, {
      startY: y,
      head: [['Parameter', 'Proposed', 'Permitted / Min', 'Status']],
      body: tableRows,
      headStyles: { fillColor: BRAND, textColor: [255, 255, 255], fontSize: 8 },
      bodyStyles: { fontSize: 8 },
      columnStyles: { 3: { halign: 'center' } },
      didParseCell: (data) => {
        if (data.column.index === 3 && data.section === 'body') {
          const txt = data.cell.text[0] ?? ''
          data.cell.styles.textColor = txt.startsWith('✓') ? [22, 163, 74] : [220, 38, 38]
          data.cell.styles.fontStyle = 'bold'
        }
      },
    })
    y = doc.lastAutoTable.finalY + 6

    if (r.violations?.length) {
      doc.setFontSize(8)
      doc.setFont('helvetica', 'bold')
      doc.text('Non-conformances:', 12, y); y += 5
      doc.setFont('helvetica', 'normal')
      r.violations.forEach((v) => {
        const msg = typeof v === 'string' ? v : (v.message ?? JSON.stringify(v))
        doc.text(`• ${msg}`, 14, y); y += 5
        if (y > 270) { doc.addPage(); y = 20 }
      })
    }
  }

  // ── Energy Compliance (SANS 10400-XA) ──────────────────────────────────────
  if (energy?.result) {
    y += 4
    if (y > 230) { doc.addPage(); y = drawHeader(doc, projectName, 'Energy Compliance') }
    y = drawSection(doc, 'SANS 10400-XA Energy Compliance', y)

    const er = energy.result
    const ep = energy.params ?? {}

    doc.setFontSize(10)
    doc.setFont('helvetica', 'bold')
    doc.setTextColor(...(er.isCompliant ? [22, 163, 74] : [220, 38, 38]))
    doc.text(er.isCompliant ? '✓  ENERGY: COMPLIANT' : '✗  ENERGY: NON-COMPLIANT', 12, y)
    doc.setTextColor(...DARK)
    y += 8

    autoTable(doc, {
      startY: y,
      head: [['Check', 'Value', 'Limit', 'Status']],
      body: (er.checks ?? []).map((c) => [
        c.description ?? c.rule ?? '',
        c.providedValue != null ? Number(c.providedValue).toFixed(2) + ' ' + (c.unit ?? '') : '—',
        c.requiredValue != null ? Number(c.requiredValue).toFixed(2) + ' ' + (c.unit ?? '') : '—',
        passFailBadge(c.passed),
      ]),
      headStyles: { fillColor: BRAND, textColor: [255, 255, 255], fontSize: 8 },
      bodyStyles: { fontSize: 8 },
      columnStyles: { 3: { halign: 'center' } },
      didParseCell: (data) => {
        if (data.column.index === 3 && data.section === 'body') {
          const txt = data.cell.text[0] ?? ''
          data.cell.styles.textColor = txt.startsWith('✓') ? [22, 163, 74] : [220, 38, 38]
          data.cell.styles.fontStyle = 'bold'
        }
      },
    })
    y = doc.lastAutoTable.finalY + 6
  }

  // ── Door Schedule ──────────────────────────────────────────────────────────
  const doors = schedule?.doors ?? []
  if (doors.length) {
    if (y > 220) { doc.addPage(); y = drawHeader(doc, projectName, 'Door Schedule') }
    else y += 4
    y = drawSection(doc, `Door Schedule (${doors.length} items)`, y)

    autoTable(doc, {
      startY: y,
      head: [['Mark', 'Name / Type', 'Width (mm)', 'Height (mm)', 'Area (m²)', 'Level']],
      body: doors.map((d) => [
        d.mark, d.name || d.type || '—',
        d.widthMm ?? '—', d.heightMm ?? '—',
        d.areaM2 != null ? Number(d.areaM2).toFixed(2) : '—',
        d.level ?? '—',
      ]),
      headStyles: { fillColor: BRAND, textColor: [255, 255, 255], fontSize: 8 },
      bodyStyles: { fontSize: 7.5 },
      alternateRowStyles: { fillColor: LIGHT },
    })
    y = doc.lastAutoTable.finalY + 6
  }

  // ── Window Schedule ────────────────────────────────────────────────────────
  const windows = schedule?.windows ?? []
  if (windows.length) {
    if (y > 220) { doc.addPage(); y = drawHeader(doc, projectName, 'Window Schedule') }
    else y += 4
    y = drawSection(doc, `Window Schedule (${windows.length} items)`, y)

    autoTable(doc, {
      startY: y,
      head: [['Mark', 'Name / Type', 'Width (mm)', 'Height (mm)', 'Area (m²)', 'Level']],
      body: windows.map((w) => [
        w.mark, w.name || w.type || '—',
        w.widthMm ?? '—', w.heightMm ?? '—',
        w.areaM2 != null ? Number(w.areaM2).toFixed(2) : '—',
        w.level ?? '—',
      ]),
      headStyles: { fillColor: BRAND, textColor: [255, 255, 255], fontSize: 8 },
      bodyStyles: { fontSize: 7.5 },
      alternateRowStyles: { fillColor: LIGHT },
    })
    y = doc.lastAutoTable.finalY + 6
  }

  // ── Fixture Tally / BOQ ────────────────────────────────────────────────────
  const tallyItems = tally?.items ?? []
  if (tallyItems.length) {
    doc.addPage()
    y = drawHeader(doc, projectName, 'Fixture Tally — Bill of Quantities')
    y = drawSection(doc, `Fixture & MEP Tally (${tallyItems.length} items)`, y)

    // Summary counts
    y = drawInfoGrid(doc, [
      ['Lighting',    tally.lightingCount ?? 0],
      ['Electrical',  tally.electricalCount ?? 0],
      ['Sanitary',    tally.sanitaryCount ?? 0],
      ['HVAC',        tally.hvacCount ?? 0],
      ['Fire Safety', tally.fireCount ?? 0],
      ['Other',       tally.otherCount ?? 0],
    ], y)

    autoTable(doc, {
      startY: y,
      head: [['Category', 'IFC Type', 'Mark', 'Name', 'Level']],
      body: tallyItems.slice(0, 800).map((i) => [
        i.category, i.ifcType, i.mark, i.name || '—', i.level ?? '—',
      ]),
      headStyles: { fillColor: BRAND, textColor: [255, 255, 255], fontSize: 8 },
      bodyStyles: { fontSize: 7 },
      alternateRowStyles: { fillColor: LIGHT },
    })
    y = doc.lastAutoTable.finalY + 6
  }

  // ── Specification Clauses ──────────────────────────────────────────────────
  const sections = spec?.sections ?? []
  if (sections.length) {
    doc.addPage()
    y = drawHeader(doc, projectName, 'Construction Specification')

    sections.forEach((sec) => {
      if (!sec.clauses?.length) return
      if (y > 240) { doc.addPage(); y = drawHeader(doc, projectName, 'Construction Specification') }
      y = drawSection(doc, sec.name, y)
      sec.clauses.forEach((clause, i) => {
        if (y > 255) { doc.addPage(); y = 20 }
        doc.setFontSize(8)
        doc.setFont('helvetica', 'bold')
        doc.text(`${i + 1}.  ${clause.heading}`, 12, y)
        doc.setFont('helvetica', 'italic')
        doc.setTextColor(...BRAND)
        doc.text(clause.reference ?? '', doc.internal.pageSize.getWidth() - 12, y, { align: 'right' })
        doc.setTextColor(...DARK)
        y += 5
        doc.setFont('helvetica', 'normal')
        const lines = doc.splitTextToSize(clause.text ?? '', doc.internal.pageSize.getWidth() - 24)
        doc.text(lines, 14, y)
        y += lines.length * 4.2 + 4
      })
    })
  }

  addFooters(doc, projectName)

  const filename = `${projectName.replace(/[^a-zA-Z0-9]/g, '_')}_Council_Report_${new Date().toISOString().slice(0, 10)}.pdf`
  doc.save(filename)
}

