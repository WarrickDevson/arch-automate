/**
 * Generates and downloads a QS-ready Excel workbook (.xlsx).
 * Each data domain gets its own worksheet tab.
 *
 * @param {Object} payload  Same shape as exportPdf()
 */
export function exportExcel(payload) {
  const { project, zoning, energy, schedule, tally, spec } = payload
  const projectName = project?.name ?? 'Project'
  const wb = XLSX.utils.book_new()

  // ── Summary sheet ──────────────────────────────────────────────────────────
  const summaryRows = [
    ['ARCHAUTOMATE — PROJECT EXPORT'],
    [],
    ['Project Name', project?.name ?? '—'],
    ['Municipality',  project?.municipality ?? '—'],
    ['Erf Number',    project?.erfNumber ?? '—'],
    ['Zoning Scheme', project?.zoningScheme ?? '—'],
    ['Site Area (m²)', project?.siteAreaM2 ?? '—'],
    ['Export Date',   today()],
  ]
  const wsSummary = XLSX.utils.aoa_to_sheet(summaryRows)
  wsSummary['!cols'] = [{ wch: 22 }, { wch: 40 }]
  XLSX.utils.book_append_sheet(wb, wsSummary, 'Project Summary')

  // ── Zoning sheet ──────────────────────────────────────────────────────────
  if (zoning?.result || zoning?.params) {
    const r = zoning.result ?? {}
    const p = zoning.params ?? {}
    const s = zoning.scheme ?? {}
    const zoningRows = [
      ['Municipal Zoning Compliance'],
      ['Zoning Designation', s.name ?? project?.zoningScheme ?? '—'],
      [],
      ['Parameter', 'Proposed', 'Permitted / Min', 'Status'],
      ...(r.checks ?? []).map((c) => [
        c.description ?? c.rule ?? '',
        c.providedValue != null ? `${Number(c.providedValue).toFixed(2)} ${c.unit ?? ''}`.trim() : '—',
        c.requiredValue != null ? `${Number(c.requiredValue).toFixed(2)} ${c.unit ?? ''}`.trim() : '—',
        c.passed ? 'PASS' : 'FAIL',
      ]),
      [],
      ['Overall Compliance', r.isCompliant != null ? (r.isCompliant ? 'COMPLIANT' : 'NON-COMPLIANT') : '—'],
      [],
      ['Violations / Non-conformances'],
      ...(r.violations ?? []).map((v) => [typeof v === 'string' ? v : (v.message ?? '')]),
    ]
    const wsZoning = XLSX.utils.aoa_to_sheet(zoningRows)
    wsZoning['!cols'] = [{ wch: 24 }, { wch: 16 }, { wch: 14 }, { wch: 14 }]
    XLSX.utils.book_append_sheet(wb, wsZoning, 'Zoning Compliance')
  }

  // ── Energy sheet ───────────────────────────────────────────────────────────
  if (energy?.result) {
    const er = energy.result
    const ep = energy.params ?? {}
    const energyRows = [
      ['SANS 10400-XA Energy Compliance'],
      [],
      ['Parameter', 'Value', 'Limit', 'Status'],
      ...(er.checks ?? []).map((c) => [
        c.description ?? c.rule ?? '',
        c.providedValue != null ? `${Number(c.providedValue).toFixed(2)} ${c.unit ?? ''}`.trim() : '—',
        c.requiredValue != null ? `${Number(c.requiredValue).toFixed(2)} ${c.unit ?? ''}`.trim() : '—',
        c.passed ? 'PASS' : 'FAIL',
      ]),
      [],
      ['Overall', '', '', er.isCompliant ? 'COMPLIANT' : 'NON-COMPLIANT'],
      [],
      ['Climate Zone', ep?.climaticZone ?? er.climateZone ?? '—'],
      ['Energy Rating', er.energyRating ?? '—'],
      ['Window-to-Wall Ratio (%)', er.windowToWallRatioPercent != null ? pct(er.windowToWallRatioPercent) : '—'],
    ]
    const wsEnergy = XLSX.utils.aoa_to_sheet(energyRows)
    wsEnergy['!cols'] = [{ wch: 32 }, { wch: 16 }, { wch: 16 }, { wch: 14 }]
    XLSX.utils.book_append_sheet(wb, wsEnergy, 'Energy (XA)')
  }

  // ── Door Schedule sheet ────────────────────────────────────────────────────
  const doors = schedule?.doors ?? []
  if (doors.length) {
    const doorRows = [
      ['Door Schedule', '', '', '', '', ''],
      ['Mark', 'Name', 'Type', 'Width (mm)', 'Height (mm)', 'Area (m²)', 'Level'],
      ...doors.map((d) => [
        d.mark, d.name ?? '', d.type ?? '',
        d.widthMm ?? '', d.heightMm ?? '',
        d.areaM2 != null ? Number(d.areaM2).toFixed(3) : '',
        d.level ?? '',
      ]),
    ]
    const wsDoors = XLSX.utils.aoa_to_sheet(doorRows)
    wsDoors['!cols'] = [{ wch: 10 }, { wch: 20 }, { wch: 16 }, { wch: 12 }, { wch: 13 }, { wch: 10 }, { wch: 14 }]
    XLSX.utils.book_append_sheet(wb, wsDoors, 'Doors')
  }

  // ── Window Schedule sheet ──────────────────────────────────────────────────
  const windows = schedule?.windows ?? []
  if (windows.length) {
    const winRows = [
      ['Window Schedule', '', '', '', '', ''],
      ['Mark', 'Name', 'Type', 'Width (mm)', 'Height (mm)', 'Area (m²)', 'Level'],
      ...windows.map((w) => [
        w.mark, w.name ?? '', w.type ?? '',
        w.widthMm ?? '', w.heightMm ?? '',
        w.areaM2 != null ? Number(w.areaM2).toFixed(3) : '',
        w.level ?? '',
      ]),
    ]
    const wsWin = XLSX.utils.aoa_to_sheet(winRows)
    wsWin['!cols'] = [{ wch: 10 }, { wch: 20 }, { wch: 16 }, { wch: 12 }, { wch: 13 }, { wch: 10 }, { wch: 14 }]
    XLSX.utils.book_append_sheet(wb, wsWin, 'Windows')
  }

  // ── Fixture Tally / BOQ sheet ──────────────────────────────────────────────
  const tallyItems = tally?.items ?? []
  if (tallyItems.length) {
    const tallyRows = [
      ['Fixture Tally — Bill of Quantities (BOQ)'],
      [],
      ['Category Summary'],
      ['Lighting', tally?.lightingCount ?? 0],
      ['Electrical', tally?.electricalCount ?? 0],
      ['Sanitary', tally?.sanitaryCount ?? 0],
      ['HVAC', tally?.hvacCount ?? 0],
      ['Fire Safety', tally?.fireCount ?? 0],
      ['Other', tally?.otherCount ?? 0],
      ['TOTAL', tally?.totalCount ?? tallyItems.length],
      [],
      ['Category', 'IFC Type', 'Mark', 'Name', 'Level'],
      ...tallyItems.map((i) => [
        i.category, i.ifcType, i.mark, i.name ?? '', i.level ?? '',
      ]),
    ]
    const wsTally = XLSX.utils.aoa_to_sheet(tallyRows)
    wsTally['!cols'] = [{ wch: 14 }, { wch: 18 }, { wch: 12 }, { wch: 28 }, { wch: 18 }]
    XLSX.utils.book_append_sheet(wb, wsTally, 'Fixture BOQ')
  }

  // ── Specification sheet ────────────────────────────────────────────────────
  const sections = spec?.sections ?? []
  if (sections.length) {
    const specRows = [['Construction Specification'], []]
    sections.forEach((sec) => {
      specRows.push([sec.name.toUpperCase()])
      specRows.push(['#', 'Clause Heading', 'Specification Text', 'SANS Reference'])
      sec.clauses.forEach((c, i) => {
        specRows.push([i + 1, c.heading, c.text, c.reference])
      })
      specRows.push([])
    })
    const wsSpec = XLSX.utils.aoa_to_sheet(specRows)
    wsSpec['!cols'] = [{ wch: 4 }, { wch: 28 }, { wch: 80 }, { wch: 30 }]
    XLSX.utils.book_append_sheet(wb, wsSpec, 'Specification')
  }

  const filename = `${projectName.replace(/[^a-zA-Z0-9]/g, '_')}_QS_Export_${new Date().toISOString().slice(0, 10)}.xlsx`
  XLSX.writeFile(wb, filename)
}
