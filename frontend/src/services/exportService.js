/**
 * exportService.js
 * Pure client-side PDF (jsPDF + jspdf-autotable) and Excel (SheetJS) export engine.
 * All functions accept plain data objects and return blobs or trigger downloads.
 *
 * Exports:
 *   exportPdf(payload)  → downloads a council-ready PDF
 *   exportExcel(payload) → downloads a QS-ready .xlsx workbook
 */

import jsPDF from 'jspdf'
import autoTable from 'jspdf-autotable'
import * as XLSX from 'xlsx'

// ── Palette ──────────────────────────────────────────────────────────────────
const BRAND = [30, 64, 175]   // indigo-800 in RGB
const DARK  = [15, 23, 42]    // slate-900
const LIGHT = [241, 245, 249] // slate-100

// ── Helpers ───────────────────────────────────────────────────────────────────

function today() {
  return new Date().toLocaleDateString('en-ZA', {
    day: '2-digit', month: 'long', year: 'numeric',
  })
}

function pct(v) {
  return v != null ? `${Number(v).toFixed(1)}%` : '—'
}
function num(v, dp = 2) {
  return v != null ? Number(v).toFixed(dp) : '—'
}
function passFailBadge(pass) {
  return pass ? '✓ PASS' : '✗ FAIL'
}

// ── PDF helpers ───────────────────────────────────────────────────────────────

/**
 * Draws the standard page header (logo placeholder, project name, date).
 * Returns the Y position after the header.
 */
function drawHeader(doc, projectName, subtitle) {
  const W = doc.internal.pageSize.getWidth()
  // Navy header bar
  doc.setFillColor(...BRAND)
  doc.rect(0, 0, W, 28, 'F')

  doc.setTextColor(255, 255, 255)
  doc.setFont('helvetica', 'bold')
  doc.setFontSize(13)
  doc.text('ArchAutomate', 12, 11)
  doc.setFont('helvetica', 'normal')
  doc.setFontSize(8)
  doc.text('South African Architect Post-Design Portal', 12, 17)

  doc.setFont('helvetica', 'bold')
  doc.setFontSize(11)
  doc.text(projectName, W / 2, 11, { align: 'center' })
  doc.setFont('helvetica', 'normal')
  doc.setFontSize(8)
  doc.text(subtitle, W / 2, 17, { align: 'center' })

  doc.setFontSize(8)
  doc.text(today(), W - 12, 11, { align: 'right' })
  doc.text('Confidential', W - 12, 17, { align: 'right' })

  doc.setTextColor(...DARK)
  return 36
}

/**
 * Draws a section heading.
 */
function drawSection(doc, title, y) {
  const W = doc.internal.pageSize.getWidth()
  doc.setFillColor(...LIGHT)
  doc.rect(10, y, W - 20, 7, 'F')
  doc.setFont('helvetica', 'bold')
  doc.setFontSize(9)
  doc.setTextColor(...BRAND)
  doc.text(title.toUpperCase(), 13, y + 5)
  doc.setTextColor(...DARK)
  return y + 10
}

/**
 * Draws a two-column key/value info block.
 */
function drawInfoGrid(doc, rows, y) {
  const colW = 85
  const rowH = 6
  doc.setFontSize(8)
  doc.setFont('helvetica', 'normal')

  rows.forEach(([label, value], i) => {
    const col = i % 2
    const row = Math.floor(i / 2)
    const x = 12 + col * colW
    const yy = y + row * rowH
    doc.setFont('helvetica', 'bold')
    doc.text(label + ':', x, yy)
    doc.setFont('helvetica', 'normal')
    doc.text(String(value ?? '—'), x + 38, yy)
  })

  return y + Math.ceil(rows.length / 2) * rowH + 4
}

/**
 * Adds a standard page footer to every page.
 */
function addFooters(doc, projectName) {
  const pageCount = doc.getNumberOfPages()
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i)
    const W = doc.internal.pageSize.getWidth()
    const H = doc.internal.pageSize.getHeight()
    doc.setFontSize(7)
    doc.setTextColor(150)
    doc.text(`${projectName} — Generated by ArchAutomate on ${today()}`, 12, H - 6)
    doc.text(`Page ${i} of ${pageCount}`, W - 12, H - 6, { align: 'right' })
    doc.setDrawColor(200)
    doc.line(10, H - 9, W - 10, H - 9)
  }
}

